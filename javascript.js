// Init

function init() {
    // Adiciona funções para tratar os eventos 
        
    $("#btnInsert").click(function () {
        // Verfica se o formulário está preenchido corretamente
        if (!$("#form-postit")[0].checkValidity()) {
            //displayMessage("Preencha o formulário corretamente.");
            return;
        }

        // Obtem os valores dos campos do formulário
        let campoDescricao = $("#inputDescricao").val();
        let campoUrgencia = $("#inputUrgencia").val();
        let campoDia = $("#inputDia").val();
        let campoHorario = $("#inputHorario").val();
        let campoCategoria = $("#inputCategoria").val();
        let postit = {
            descricao: campoDescricao,
            urgencia: campoUrgencia,
            dia: campoDia,
            horario: campoHorario,
            categoria: campoCategoria,
        };

        insertPostit(postit);

        // Limpa o formulario
        $("#form-postit")[0].reset();
    });

    // Intercepta o click do botão Alterar
    $("#btnUpdate").click(function () {
        // Obtem os valores dos campos do formulário
        let campoId = $("#inputId").val();
        if (campoId == "") {
            //displayMessage("Selecione um post-it para ser alterado.");
            return;
        }
        let campoDescricao = $("#inputDescricao").val();
        let campoUrgencia = $("#inputUrgencia").val();
        let campoDia = $("#inputDia").val();
        let campoHorario = $("#inputHorario").val();
        let campoCategoria = $("#inputCategoria").val();
        let postit = {
            descricao: campoDescricao,
            urgencia: campoUrgencia,
            dia: campoDia,
            horario: campoHorario,
            categoria: campoCategoria,
        };

        updatepostit(parseInt(campoId), postit);

        // Limpa o formulario
        $("#form-postit")[0].reset();
    });

    // Intercepta o click do botão Excluir
    $("#btnDelete").click(function () {
        let campoId = $("#inputId").val();
        if (campoId == "") {
            //displayMessage("Selecione um post-it a ser excluído.");
            return;
        }
        deletepostit(parseInt(campoId));

        // Limpa o formulario
        $("#form-postit")[0].reset();
    });

    // Intercepta o click do botão Cancelar
    
}


